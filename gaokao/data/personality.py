def recommend_majors_by_holland(holland_scores, top_n=5):
    matches = []
    for major, codes in MAJOR_HOLLAND_MAP.items():
        score = sum(weight * holland_scores.get(code, 0) for code, weight in codes)
        matches.append((major, score))
    matches.sort(key=lambda x: -x[1])
    return [(m, s) for m, s in matches[:top_n] if s > 0]

